#include "../cub3d.h"

int check_val(char *str)
{
    int i;

    i = 0;
    while (str[i])
    {
        if ((!ft_isdigit(str[i]) || ft_isalpha(str[i])))
            return 0;
        i++;
    }
    return 1;;
}